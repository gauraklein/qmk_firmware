bool oled_task_user(void) {
     current_wpm   = get_current_wpm();
    if (is_keyboard_master()) {
        oled_render_layer_state();
        oled_render_keylog();
    } else {
     render_luna(0, 13);
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    set_keylog(keycode, record);
  }
  switch (keycode) {
            case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

            case KC_LGUI:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;

            case KC_CAPS:
            if (record->event.pressed && isCaps) {
                isCaps = true;
            } else {
                isCaps = false;
            }
  }
  return true;
}
